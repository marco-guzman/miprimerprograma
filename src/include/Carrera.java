package include;

public class Carrera {
    private int ClaveCarre;
    private String Nombre;

    public Carrera(int claveCarre, String nombre) {
        ClaveCarre = claveCarre;
        Nombre = nombre;
    }

    public int getClaveCarre() {
        return ClaveCarre;
    }

    public String getNombre() {
        return Nombre;
    }
}



